package com.mycompany.qlsanbong;

/**
 *
 * @author Admin
 */
public class Customer {

    private int id, point;
    private String name, phone;

    public Customer() {
    }

    public Customer(int id, String name, String phone, int point) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.point = point;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
   

}
